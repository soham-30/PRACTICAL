void main() {
  int sum = 0;
  int multi = 1;
  int i = 1;

  while (i <= 10) {
    if (i % 2 == 0) {
      sum = sum = i;
      print("sum of even number is $sum");
      i++;
    } else {
      multi = multi * i;
      print("multiplication  of odd number is $multi");
      i++;
    }
  }
}
